public class Vergleichstest {
    public static void main(String[] args) {
        double a = 2.05;
        double b = 0.05;
        System.out.println(a);
        System.out.println(b);
        System.out.println(a-b);
        boolean test;
        test = (2.05-0.05) == 2.0;
        System.out.println(test);
        System.out.println(2.05-0.05);
        System.out.println(2.0);
    }
}

